public class TesteWhile {

    public static void main(String[] args) {

        int i = 1;

        while (i <= 10) {
            System.out.println(i);
            i++;
        }

        System.out.println("------------------");

        int n = 100;

        while (n >= 1) {
            System.out.println(n);
            n--;
        }
    }
}
